num1 = int(input("Nhập số nguyên thứ nhất: "))
num2 = int(input("Nhập số nguyên thứ hai: "))
print("{}  {}  {}  = {}".format(num1,"+",num2,num1+num2))
print("{}  {}  {}  = {}".format(num1,"-",num2,num1-num2))
print("{}  {}  {}  = {}".format(num1,"*",num2,num1*num2))
if num2 == 0:
    print("Không thể thực hiện phép chia!")
else:
    print("{}  {}  {}  = {}".format(num1,"/",num2,num1/num2))
    print("{}  {}  {}  = {}".format(num1,"//",num2,num1//num2))
    print("{}  {}  {}  = {}".format(num1,"%",num2,num1%num2))





 

